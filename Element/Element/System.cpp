/* INCLUDES */
#include "ElementHeader.h"

/***************************************************************************************************
* System function
*
* Placeholder.
***************************************************************************************************/
System::System( void ) {
	this->renderer = new Renderer( this );
	this->input = new Input( this );
	this->scene = new Scene( this );
}

/***************************************************************************************************
* ~System function
*
* Placeholder.
***************************************************************************************************/
System::~System( void ) {
	delete this->scene;
	delete this->input;
	delete this->renderer;
}

/***************************************************************************************************
* run function
*
* Placeholder.
***************************************************************************************************/
void System::run( void ) {
	while ( this->renderer->windowOpen() ) {
		this->input->pollInput();
		this->scene->update();
		this->renderer->draw();
	}
}